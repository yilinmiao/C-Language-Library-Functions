#include "libft.h"

char			**ft_strsplit(char const *s, char c)
{
	int		i;
	int		j;
	int		k;
	char	**str2;

	if (!s || !(str2 = (char **)malloc(sizeof(*str2) *
		(ft_strcount((char*)s, c) + 1))))
		return (NULL);
	i = 0;
	j = 0;
	while (i++ < ft_strcount((char*)s, c))
	{
		k = 0;
		if (!(str2[i] = ft_strnew(ft_get_word_len(&s[j], c) + 1)))
			str2[i] = NULL;
		while (s[j] == c)
			j++;
		while (s[j] != c && s[j])
			str2[i][k++] = s[j++];
		str2[i][k] = '\0';
	}
	str2[i] = 0;
	return (str2);
}
