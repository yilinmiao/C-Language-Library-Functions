#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*s1 && *s1 == *s2 && n--)
	{
		s1++;
		s2++;
	}
	return (n ? *(t_byte *)s1 - *(t_byte *)s2 : 0);
}
